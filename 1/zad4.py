# Napisz skrypt, który zapyta użytkownika o trzy liczby całkowite,
# a następnie pomnóż dwa pierwsze. przed podzieleniem wyniku przez trzecią liczbę. 
# Wejście i wyjście powinny być zrozumiałe dla użytkownika.
liczba1= int(input("Podaj dowolną liczbę całkowitą 1:"))
liczba2= int(input("Podaj dowolną liczbę całkowitą 2:"))
liczba3= int(input("Podaj dowolną liczbę całkowitą 3:"))
mnozenie= liczba1 * liczba2
dzielenie= mnozenie/liczba3
print("Wynik mnożenia liczby1 i liczby2 to:", mnozenie)
print ("Wynik dzielenia iloczynu liczb 1 i 2 przez liczbę 3 to:", dzielenie)


